package Beans;


public class Asistencia {

	private String idAsistencia;
	private String fecha;
	private String grupoNivelacion;
	private String clase;
	private String tema;
	private String salon;
	
	public String getIdAsistencia() {
		return idAsistencia;
	}
	public void setIdAsistencia(String idAsistencia) {
		this.idAsistencia = idAsistencia;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getGrupoNivelacion() {
		return grupoNivelacion;
	}
	public void setGrupoNivelacion(String grupoNivelacion) {
		this.grupoNivelacion = grupoNivelacion;
	}
	public String getClase() {
		return clase;
	}
	public void setClase(String clase) {
		this.clase = clase;
	}
	public String getTema() {
		return tema;
	}
	public void setTema(String tema) {
		this.tema = tema;
	}
	public String getSalon() {
		return salon;
	}
	public void setSalon(String salon) {
		this.salon = salon;
	}
	
	
}
