package Beans;

public class DetalleNivelacion {

	private String idAlumno;
	private String idNivelacion;
	
	public String getIdAlumno() {
		return idAlumno;
	}
	public void setIdAlumno(String idAlumno) {
		this.idAlumno = idAlumno;
	}
	public String getIdNivelacion() {
		return idNivelacion;
	}
	public void setIdNivelacion(String idNivelacion) {
		this.idNivelacion = idNivelacion;
	}
	
}
