package Beans;

public class Docentes {
	
	
	private String dni;
	private String nombre;
	private String apellidopat;
	private String apellidomat;
	private int edad;
	private String turno;
	private String correo;
	private String fechaContrato;
	private String fechaNacimiento;
	private String carreraPublica;
	private int titulado;
	private int magister;
	private int doctorado;
	private int telefono;
	private String nivel;
	
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidopat() {
		return apellidopat;
	}
	public void setApellidopat(String apellidopat) {
		this.apellidopat = apellidopat;
	}
	public String getApellidomat() {
		return apellidomat;
	}
	public void setApellidomat(String apellidomat) {
		this.apellidomat = apellidomat;
	}
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public String getTurno() {
		return turno;
	}
	public void setTurno(String turno) {
		this.turno = turno;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getFechaContrato() {
		return fechaContrato;
	}
	public void setFechaContrato(String fechaContrato) {
		this.fechaContrato = fechaContrato;
	}
	public String getFechaNacimiento() {
		return fechaNacimiento;
	}
	public void setFechaNacimiento(String fechaNacimiento) {
		this.fechaNacimiento = fechaNacimiento;
	}
	public String getCarreraPublica() {
		return carreraPublica;
	}
	public void setCarreraPublica(String carreraPublica) {
		this.carreraPublica = carreraPublica;
	}
	public int getTitulado() {
		return titulado;
	}
	public void setTitulado(int titulado) {
		this.titulado = titulado;
	}
	public int getMagister() {
		return magister;
	}
	public void setMagister(int magister) {
		this.magister = magister;
	}
	public int getDoctorado() {
		return doctorado;
	}
	public void setDoctorado(int doctorado) {
		this.doctorado = doctorado;
	}
	public int getTelefono() {
		return telefono;
	}
	public void setTelefono(int telefono) {
		this.telefono = telefono;
	}
	public String getNivel() {
		return nivel;
	}
	public void setNivel(String nivel) {
		this.nivel = nivel;
	}
	
}
